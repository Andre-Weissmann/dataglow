/* ================================================================
   DataGlow Gate State Exporter (Session C, PR #533)
   Feature flag: window.FEATURE_FLAGS.gateExporter

   Exports a signed JSON artifact capturing the full audit state:
     - Dataset fingerprint (SHA-256 of first 10k rows)
     - Pulse score + component breakdown
     - Bias Preflight result (pass/skip/fail + categories checked)
     - Peer Review result (pass/skip/fail + reviewer count)
     - Training Passport completeness
     - Purpose Contracts declared (redacted to intent only, no PHI)
     - Federated Quality Score composite
     - Role Context at time of export
     - All metrics defined in Metric Studio (names only, not exprs)
     - Governance skipped flags (from EthicsEscape)
     - Timestamp + session hash

   The artifact is signed with a session keypair (ECDSA P-256, generated
   in-browser, public key embedded in the artifact). This is not a
   cryptographic guarantee of identity -- it proves the artifact was
   generated by a single continuous browser session, not assembled post-hoc.

   Output: dataglow-gate-state-{timestamp}.json
   Unlocks at Tier 3 (Pulse >= 90 or FQS >= 90).
================================================================ */
(function () {
  'use strict';

  var FLAG = 'gateExporter';
  function isEnabled() { return !!(window.FEATURE_FLAGS && window.FEATURE_FLAGS[FLAG]); }

  /* ---- Session keypair (generated once, held in memory) ---- */
  var _keypair = null;

  function ensureKeypair() {
    if (_keypair) return Promise.resolve(_keypair);
    return crypto.subtle.generateKey(
      { name: 'ECDSA', namedCurve: 'P-256' },
      true,
      ['sign', 'verify']
    ).then(function (kp) {
      _keypair = kp;
      return kp;
    });
  }

  function exportPublicKey(kp) {
    return crypto.subtle.exportKey('jwk', kp.publicKey);
  }

  function signPayload(kp, payload) {
    var encoder = new TextEncoder();
    return crypto.subtle.sign(
      { name: 'ECDSA', hash: 'SHA-256' },
      kp.privateKey,
      encoder.encode(payload)
    ).then(function (sig) {
      return btoa(String.fromCharCode.apply(null, new Uint8Array(sig)));
    });
  }

  /* ---- Collect audit state from window globals ---- */
  function collectState() {
    var state = {
      schema_version: '1.0',
      generated_at:   new Date().toISOString(),
      session_id:     _sessionHash(),
      dataset: {
        fingerprint:  (window.DataGlowDataset && window.DataGlowDataset.fingerprint) || null,
        row_count:    (window.DataGlowDataset && window.DataGlowDataset.rowCount) || null,
        col_count:    (window.DataGlowDataset && window.DataGlowDataset.colCount) || null
      },
      pulse: (window.PulseInterpreter && window.PulseInterpreter.getScore) ?
        window.PulseInterpreter.getScore() : null,
      federated_quality_score: (window.FederatedQualityScore && window.FederatedQualityScore.compute) ?
        window.FederatedQualityScore.compute() : null,
      fqs_breakdown: (window.FederatedQualityScore && window.FederatedQualityScore.getBreakdown) ?
        window.FederatedQualityScore.getBreakdown() : null,
      bias_preflight: _collectBiasPreflight(),
      peer_review:    _collectPeerReview(),
      purpose_contracts: _collectPurposeContracts(),
      training_passport: _collectPassport(),
      metrics_defined: (window.MetricStudio && window.MetricStudio.list) ?
        window.MetricStudio.list().map(function (m) { return m.name; }) : [],
      role_context: (window.RoleContext && window.RoleContext.current) ?
        window.RoleContext.current() : null,
      governance_skipped: (window.EthicsEscape && window.EthicsEscape.getSkipped) ?
        window.EthicsEscape.getSkipped() : [],
      flag_tier: _currentTier()
    };
    return state;
  }

  function _sessionHash() {
    var ts = performance.timeOrigin || Date.now();
    return 'dg-' + Math.abs(Math.round(ts)).toString(36);
  }

  function _currentTier() {
    var body = document.body;
    if (body.classList.contains('tier-3-unlocked')) return 3;
    if (body.classList.contains('tier-2-unlocked')) return 2;
    if (body.classList.contains('tier-1-unlocked')) return 1;
    return 0;
  }

  function _collectBiasPreflight() {
    if (window.BiasPreflightResult) return window.BiasPreflightResult;
    var el = document.querySelector('[data-bias-result]');
    return el ? el.getAttribute('data-bias-result') : 'not_run';
  }

  function _collectPeerReview() {
    if (window.PeerReviewResult) return window.PeerReviewResult;
    var el = document.querySelector('[data-peer-result]');
    return el ? el.getAttribute('data-peer-result') : 'not_run';
  }

  function _collectPurposeContracts() {
    if (window.PurposeContracts && window.PurposeContracts.getSummary) {
      return window.PurposeContracts.getSummary();
    }
    return 'not_declared';
  }

  function _collectPassport() {
    if (window.TrainingPassport && window.TrainingPassport.getCompleteness) {
      return window.TrainingPassport.getCompleteness();
    }
    return null;
  }

  /* ---- Export ---- */
  function exportGateState() {
    return ensureKeypair().then(function (kp) {
      var state = collectState();
      var payload = JSON.stringify(state, null, 2);
      return Promise.all([
        exportPublicKey(kp),
        signPayload(kp, payload)
      ]).then(function (results) {
        var artifact = {
          artifact_type: 'dataglow-gate-state',
          schema_version: '1.0',
          payload: state,
          signature: {
            algorithm: 'ECDSA-P256-SHA256',
            public_key_jwk: results[0],
            value: results[1],
            note: 'Proves single-session provenance. Not an identity assertion.'
          }
        };
        var blob = new Blob([JSON.stringify(artifact, null, 2)], { type: 'application/json' });
        var url  = URL.createObjectURL(blob);
        var a = document.createElement('a');
        a.href = url;
        a.download = 'dataglow-gate-state-' + Date.now() + '.json';
        document.body.appendChild(a);
        a.click();
        document.body.removeChild(a);
        URL.revokeObjectURL(url);

        document.dispatchEvent(new CustomEvent('dataglow:gate-exported', {
          detail: { score: state.federated_quality_score, tier: state.flag_tier }
        }));

        return artifact;
      });
    });
  }

  /* ---- Inject export button (Tier 3 only) ---- */
  function injectExportBtn() {
    if (document.getElementById('dg-gate-export-btn')) return;
    var btn = document.createElement('button');
    btn.id = 'dg-gate-export-btn';
    btn.className = 'dg-gate-export-btn';
    btn.setAttribute('data-testid', 'button-gate-export');
    btn.setAttribute('data-flag-tier', '3');
    btn.setAttribute('aria-label', 'Export gate state artifact');
    btn.title = 'Download signed audit artifact (JSON)';
    btn.innerHTML = '<svg viewBox="0 0 16 16" width="13" height="13" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M8 2v8M5 7l3 3 3-3M3 13h10"/></svg> Export Gate State';
    btn.addEventListener('click', function () {
      btn.disabled = true;
      btn.textContent = 'Generating...';
      exportGateState().then(function () {
        btn.disabled = false;
        btn.innerHTML = '<svg viewBox="0 0 16 16" width="13" height="13" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M8 2v8M5 7l3 3 3-3M3 13h10"/></svg> Export Gate State';
        if (typeof window.showToast === 'function') {
          window.showToast('Gate state artifact downloaded.', 'success');
        }
      }).catch(function () {
        btn.disabled = false;
        btn.textContent = 'Export failed';
      });
    });

    /* Place in the Output / proof panel area */
    var targets = ['#proof-chain-panel', '#output-panel', '#dg-proof-chain', '.proof-actions', '#dg-export-area'];
    var container = null;
    for (var i = 0; i < targets.length; i++) {
      container = document.querySelector(targets[i]);
      if (container) break;
    }
    if (!container) container = document.body;
    container.appendChild(btn);
  }

  function init() {
    if (!isEnabled()) return;

    document.addEventListener('dataglow:tier-3-unlocked', injectExportBtn);
    document.addEventListener('dataglow:fqs-updated', function (e) {
      if (e.detail && e.detail.score >= 90) injectExportBtn();
    });

    window.GateExporter = { export: exportGateState, collectState: collectState };
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

})();
/* ---- end js/compliance/gate-exporter.js ---- */
