// ---- mergeStores ----

await test('mergeStores deduplicates records with the same id', () => {
  let store = createMemoryStore({ sessionId: 'shared' });
  store = appendRecord(store, rec({ type: RECORD_TYPES.FILE_LOADED }));
  const storeA = store;
  const storeB = store; // same object reference — simulates two peers who haven't diverged yet
  const merged = mergeStores(storeA, storeB);
  assert.equal(merged.records.length, 1);
});

await test('mergeStores preserves full history from both peers when they diverge', () => {
  let base = createMemoryStore({ sessionId: 'base' });
  base = appendRecord(base, rec({ type: RECORD_TYPES.FILE_LOADED }));

  let peerA = appendRecord(base, rec({ type: RECORD_TYPES.MANUAL_EDIT, column: 'x' }));
  let peerB = appendRecord(base, rec({ type: RECORD_TYPES.MANUAL_EDIT, column: 'y' }));

  const merged = mergeStores(peerA, peerB);
  // base record (1) + peerA's unique edit (1) + peerB's unique edit (1) = 3
  assert.equal(merged.records.length, 3);
  assert.ok(merged.records.some((r) => r.column === 'x'));
  assert.ok(merged.records.some((r) => r.column === 'y'));
});

await test('mergeStores sorts merged records by timestamp', async () => {
  let peerA = createMemoryStore();
  peerA = appendRecord(peerA, rec({ type: RECORD_TYPES.FILE_LOADED }));
  await sleep(5);
  let peerB = createMemoryStore();
  peerB = appendRecord(peerB, rec({ type: RECORD_TYPES.SQL_QUERY, sql: 'SELECT 1' }));

  const merged = mergeStores(peerA, peerB);
  for (let i = 1; i < merged.records.length; i++) {
    assert.ok(merged.records[i - 1].timestamp <= merged.records[i].timestamp);
  }
});

await test('mergeStores handles empty stores gracefully', () => {
  const merged = mergeStores(createMemoryStore(), createMemoryStore());
  assert.deepEqual(merged.records, []);
});

