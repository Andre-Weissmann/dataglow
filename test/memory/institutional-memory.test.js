// ---- importNDJSON ----

await test('importNDJSON round-trips correctly', () => {
  const store = buildMixedStore();
  const ndjson = exportNDJSON(store, 'ds1');
  const imported = importNDJSON(ndjson);
  assert.equal(imported.records.length, 5);
  const originalIds = new Set(queryRecords(store, { datasetId: 'ds1' }).map((r) => r.id));
  for (const r of imported.records) {
    assert.ok(originalIds.has(r.id));
  }
});

await test('importNDJSON rejects malformed lines gracefully (skips, does not throw)', () => {
  const store = buildMixedStore();
  const validLine = JSON.stringify(store.records[0]);
  const ndjson = [
    validLine,
    '{not valid json',
    '',
    '   ',
    JSON.stringify({ id: 'x', type: 'bogus_type', actor: 'human', timestamp: new Date().toISOString() }),
    JSON.stringify({ id: 'y', actor: 'human', timestamp: new Date().toISOString() }), // missing type
  ].join('\n');

  let imported;
  assert.doesNotThrow(() => {
    imported = importNDJSON(ndjson);
  });
  assert.equal(imported.records.length, 1);
  assert.equal(imported.records[0].id, store.records[0].id);
});

await test('importNDJSON on empty/whitespace string returns an empty store', () => {
  const imported = importNDJSON('');
  assert.deepEqual(imported.records, []);
  const imported2 = importNDJSON('   \n  \n');
  assert.deepEqual(imported2.records, []);
});

await test('importNDJSON sorts imported records by timestamp', async () => {
  let store = createMemoryStore();
  store = appendRecord(store, rec({ type: RECORD_TYPES.FILE_LOADED }));
  await sleep(5);
  store = appendRecord(store, rec({ type: RECORD_TYPES.SQL_QUERY, sql: 'SELECT 1' }));

  // Export in normal order, then shuffle the NDJSON lines before importing.
  const ndjson = exportNDJSON(store, undefined);
  const shuffled = ndjson.split('\n').reverse().join('\n');
  const imported = importNDJSON(shuffled);

  assert.equal(imported.records[0].type, RECORD_TYPES.FILE_LOADED);
  assert.equal(imported.records[1].type, RECORD_TYPES.SQL_QUERY);
});

await test('importNDJSON rejects records with an invalid actor', () => {
  const line = JSON.stringify({ id: 'z', type: RECORD_TYPES.MANUAL_EDIT, actor: 'robot', timestamp: new Date().toISOString() });
  const imported = importNDJSON(line);
  assert.equal(imported.records.length, 0);
});

